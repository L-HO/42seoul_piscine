int	ft_sqrt(int nb)
{
	int i;

	if (nb == 1)
		return (nb);
	i = 2;
	if (nb <= 0)
		return (0);
	while (i < 46341 && i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
